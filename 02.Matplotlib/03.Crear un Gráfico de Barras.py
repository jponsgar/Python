import matplotlib.pyplot as plt

# Datos de ejemplo
categorias = ['A', 'B', 'C', 'D']
valores = [5, 7, 3, 8]

# Crear el gráfico de barras
plt.bar(categorias, valores)

# Añadir título y etiquetas
plt.title('Gráfico de Barras')
plt.xlabel('Categorías')
plt.ylabel('Valores')

# Mostrar el gráfico
plt.show()

