import matplotlib.pyplot as plt

# Datos de ejemplo
x = [1, 2, 3, 4, 5]
y = [2, 3, 5, 7, 11]

# Crear el gráfico de dispersión
plt.scatter(x, y)

# Añadir título y etiquetas
plt.title('Gráfico de Dispersión')
plt.xlabel('X')
plt.ylabel('Y')

# Mostrar el gráfico
plt.show()

